def test_helper_returns_standard_shape_when_no_output_dir(tmp_path,
                                                            monkeypatch):
    """When the output dir doesn't exist for the RFQ, helper returns
    ok=False with an error string — never crashes."""
    from src.api.modules.routes_rfq_admin import _reconcile_rfq_files_to_disk
    # Point DATA_DIR to a tmp path with no output subdirs
    from src.api.modules import routes_rfq_admin as _rra
    monkeypatch.setattr(_rra, "DATA_DIR", str(tmp_path))
    result = _reconcile_rfq_files_to_disk("rfq_nonexistent", "FAKE-SOL")
    assert result["ok"] is False
    assert result["added"] == 0
    assert result["files_added"] == []
    assert result["skipped"] == 0
    assert any("No output directory" in e for e in result["errors"])


