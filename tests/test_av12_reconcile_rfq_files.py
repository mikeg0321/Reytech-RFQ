"""PR-AV12 (AV-12) — generated-files ledger drift reconciler.

Closes AV-12 from the 5/14 EOD substrate backlog. The handoff said:

  AV-12 | Generated-files ledger drift (db_files missing entries that
       exist on disk) | Medium

`/api/rfq/<rid>/package-diag` already surfaces the drift: it returns
both `db_files` (rows in the rfq_files table) and `output_dir_files`
(literal `os.listdir` on data/output/<sol>). On records generated by
older code paths these don't agree — files exist on disk but never
got a ledger row inserted. Downstream consumers that enumerate via
`list_rfq_files` (Gmail draft attachments, file-download routes,
audit views) silently miss those files.

THE FIX

New `_reconcile_rfq_files_to_disk(rid, sol)` helper + admin route
`POST /api/admin/rfq/<rid>/reconcile-files` that:

  1. Walks the per-RFQ output directory
     (data/output/<sol> with rid fallback for legacy)
  2. Lists existing `rfq_files` ledger rows for this rid
  3. For each *.pdf on disk NOT in the ledger, reads bytes and
     calls `save_rfq_file(rid, filename, "pdf", data,
     category="generated", uploaded_by="reconcile")`

Idempotent: `save_rfq_file` dedups by (rfq_id, filename, category),
so re-running converges to the same state — no risk of inserting
duplicate rows.

Tests pin the contract without standing up the full DB:
  - Helper exists with the expected signature
  - Helper returns the standard shape (ok, added, files_added,
    skipped, errors)
  - No output directory → ok=False, no crash
  - Empty directory → ok=True, added=0
  - Disk-only PDFs are detected (when ledger comparison finds
    them missing)
  - Source-grep guards for the admin route + AV-12 marker
"""
from __future__ import annotations

