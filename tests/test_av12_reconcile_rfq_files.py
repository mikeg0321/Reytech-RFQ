def test_helper_adds_disk_only_pdfs(tmp_path, monkeypatch):
    """The substrate fix: PDFs on disk but not in the ledger get
    save_rfq_file invocations."""
    from src.api.modules.routes_rfq_admin import _reconcile_rfq_files_to_disk

    sol = "PREQ-TEST"
    out_dir = tmp_path / "output" / sol
    out_dir.mkdir(parents=True)
    (out_dir / "10847262_703C_Reytech.pdf").write_bytes(b"%PDF-fake-1")
    (out_dir / "10847262_704B_Reytech.pdf").write_bytes(b"%PDF-fake-2")
    (out_dir / "10847262_BidPackage_Reytech.pdf").write_bytes(b"%PDF-fake-3")

    from src.api.modules import routes_rfq_admin as _rra
    monkeypatch.setattr(_rra, "DATA_DIR", str(tmp_path))
    # Empty ledger — all 3 disk files are "drift"
    monkeypatch.setattr(
        "src.api.dashboard.list_rfq_files",
        lambda rid, category=None: [],
    )
    save_calls = []
    monkeypatch.setattr(
        "src.api.dashboard.save_rfq_file",
        lambda **kw: save_calls.append(kw) or "rf_test",
    )

    result = _reconcile_rfq_files_to_disk("rfq_test", sol)
    assert result["ok"] is True
    assert result["added"] == 3
    assert set(result["files_added"]) == {
        "10847262_703C_Reytech.pdf",
        "10847262_704B_Reytech.pdf",
        "10847262_BidPackage_Reytech.pdf",
    }
    assert len(save_calls) == 3
    # All inserts use category="generated" (operator can distinguish
    # ledger-reconciled rows from original-ingest buyer_attachments)
    for call in save_calls:
        assert call.get("category") == "generated"
        assert call.get("uploaded_by") == "reconcile"
        assert call.get("file_type") == "pdf"


